package 호텔;

public class Board {

	
	//1. 필드
	String id;
	String password;
	String name;
	String phone;
	String r_type;
	String r_price;
	int r_num;
	
	
	
	//2. 생성자
	public Board() {}



	public Board(String id,  String password, String name, String phone, String r_type, String r_price, int r_num) {
		super();
		this.id = id;
		this.password = password;
		this.name = name;
		this.phone = phone;
		this.r_type = r_type;
		this.r_price = r_price;
		this.r_num = r_num;
	}
	
	
	
	//3. 메소드
	
	
	
	
	
	
}
